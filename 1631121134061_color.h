#define red_n   "\033[0;31m"        // 0 -> normal ;  31 -> red 
#define red_b  "\033[1;31m"        // 1 -> bold ;
#define red_u "\033[4;31m"        // 4 -> underline ;
#define red_s  "\033[9;31m"        // 9 -> strike ;

#define cyan_n  "\033[0;36m"        //36 -> cyan 
#define cyan_b  "\033[1;36m"        
#define cyan_u "\033[4;36m"    
#define cyan_s  "\033[9;36m"    

#define green_n "\033[0;32m"        //  32 -> green
#define green_b  "\033[1;32m"        
#define green_u "\033[4;32m"    
#define green_s  "\033[9;32m"

#define blue_n  "\033[0;34m"        // 34 -> blue
#define blue_b  "\033[1;34m"
#define blue_u  "\033[4;34m"
#define blue_s  "\033[9;34m"
 
#define black  "\033[0;30m"
#define brown  "\033[0;33m"
#define magenta  "\033[0;35m"
#define gray  "\033[0;37m"
 
#define none   "\033[0m"        /* to flush the previous property */
